def get_persona(scenario_id: str) -> Persona:
    persona = PERSONAS.get(scenario_id)
    if persona is None:
        available = ", ".join(PERSONAS.keys())
        raise ValueError(f"Unknown scenario_id '{scenario_id}'. Available: {available}")
    return persona
