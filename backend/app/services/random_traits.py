def pick_trait(role_type: str) -> str:
    """Return a random trait string for the given role_type.

    Falls back to an empty string for unknown role types so callers never
    need to handle a missing-key error.
    """
    pool = TRAIT_POOLS.get(role_type)
    if not pool:
        return ""
    return random.choice(pool)


